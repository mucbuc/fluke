#include "debug.h"

namespace toolbag
{
	/////////////////////////////////////////////////////////////////////////////////////////////
	// message
	/////////////////////////////////////////////////////////////////////////////////////////////
	message::message()
	: msg( 0 )
	{}
		
	/////////////////////////////////////////////////////////////////////////////////////////////
	message::message(const message & c) 
	: msg( c.msg->copy() )
	{}

	/////////////////////////////////////////////////////////////////////////////////////////////
	message::~message()
	{	delete msg; }
		
	/////////////////////////////////////////////////////////////////////////////////////////////
	void message::operator()()
	{ 	msg->print(); }
}